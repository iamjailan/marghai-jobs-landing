export type Province = typeof AFGHANISTAN_PROVINCES[number];

export const getProvinceByName = (name: string): Province | undefined => {
  return AFGHANISTAN_PROVINCES.find(province => 
    province.name === name || 
    province.name_en === name ||
    province.name_ps === name ||
    province.name_dr === name
  );
};

export const getProvinceByCode = (code: string): Province | undefined => {
  return AFGHANISTAN_PROVINCES.find(province => province.code === code);
};

